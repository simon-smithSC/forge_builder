#!/usr/bin/env node
// Downloads the course-theme WOFF2 binaries for every embeddable face in
// courseFontCatalog (packages/player/src/fonts.ts) and regenerates the
// editor's checked-in @font-face sheet so catalog and css cannot drift.
// The build sandbox has no network; run on a machine that does:
//
//     node packages/player/scripts/fetch-course-fonts.mjs
//
// Outputs:
//   packages/player/public/fonts/<fontsourceId>-<weight>.woff2
//     (vite publicDir copies these into editor public/player-runtime/fonts/
//      on the next: pnpm --filter @forge/player build:runtime)
//   packages/editor/src/ui/course-fonts.css  (regenerated every run)
//
// Flags:
//   --force     re-download files that already exist (default: skip them)
//   --css-only  regenerate course-fonts.css without touching the network
//
// All faces are SIL OFL 1.1; sources are Fontsource's static latin builds on
// the same jsDelivr CDN packages/ui/scripts/fetch-fonts.mjs already uses.
// Until the binaries exist, every surface keeps working on the catalog's
// fallback stacks (publish emits font_missing warnings instead of failing).
import { existsSync, mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const outDir = join(here, "..", "public", "fonts");
const editorCssPath = join(here, "..", "..", "editor", "src", "ui", "course-fonts.css");
const distFonts = join(here, "..", "dist", "fonts.js");

if (!existsSync(distFonts)) {
  console.error(
    "packages/player/dist/fonts.js not found. Build @forge/player first:\n" +
      "    pnpm --filter @forge/player build",
  );
  process.exit(1);
}
const { allCourseFontFiles, buildFontFaceCss } = await import(
  new URL(`file://${distFonts}`).href
);

// PIN ME: replace "latest" with an exact Fontsource release per face after
// the first successful fetch (api.fontsource.org/v1/fonts/<id> reports the
// version) so re-fetches stay byte-stable. One line per face on purpose.
const VERSIONS = {
  "ibm-plex-sans": "latest",
  inter: "latest",
  karla: "latest",
  lato: "latest",
  "libre-baskerville": "latest",
  lora: "latest",
  manrope: "latest",
  merriweather: "latest",
  montserrat: "latest",
  nunito: "latest",
  "nunito-sans": "latest",
  "open-sans": "latest",
  "playfair-display": "latest",
  roboto: "latest",
  "source-sans-3": "latest",
  "source-serif-4": "latest",
  spectral: "latest",
  "work-sans": "latest",
};

const files = allCourseFontFiles();

// 1. Regenerate the editor's @font-face sheet from the catalog (full catalog,
// urls resolving through the editor dev server / build public dir).
const cssHeader = [
  "/* Course theme fonts for the editor canvas + preview (POLISH-PLAN V4).",
  " * GENERATED by packages/player/scripts/fetch-course-fonts.mjs from",
  " * courseFontCatalog in packages/player/src/fonts.ts - do not edit by hand.",
  " * Regenerate: node packages/player/scripts/fetch-course-fonts.mjs",
  " * Binaries live in packages/player/public/fonts and reach the editor via",
  " * build:runtime (vite publicDir) as /player-runtime/fonts/<file>.",
  " */",
  "",
  "",
].join("\n");
writeFileSync(editorCssPath, cssHeader + buildFontFaceCss(files, "/player-runtime/fonts/"));
console.log(`wrote ${editorCssPath} (${files.length} @font-face rules)`);

if (process.argv.includes("--css-only")) process.exit(0);

// 2. Fetch the binaries.
const force = process.argv.includes("--force");
mkdirSync(outDir, { recursive: true });

let failed = false;
let skipped = 0;
for (const { fontsourceId, weight, file } of files) {
  const target = join(outDir, file);
  if (!force && existsSync(target)) {
    skipped += 1;
    continue;
  }
  const version = VERSIONS[fontsourceId] ?? "latest";
  const url = `https://cdn.jsdelivr.net/fontsource/fonts/${fontsourceId}@${version}/latin-${weight}-normal.woff2`;
  try {
    const res = await fetch(url, { redirect: "follow" });
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    const buf = Buffer.from(await res.arrayBuffer());
    if (buf.length < 5_000) throw new Error(`suspiciously small (${buf.length} bytes)`);
    writeFileSync(target, buf);
    console.log(`fetched ${file} (${(buf.length / 1024).toFixed(0)} KB)`);
  } catch (error) {
    failed = true;
    console.error(`FAILED ${file} from ${url}: ${error.message}`);
  }
}

if (skipped > 0) console.log(`skipped ${skipped} existing file(s); use --force to refetch`);
if (failed) {
  console.error("\nSome fonts failed; fallback stacks keep every surface working.");
  process.exit(1);
}
console.log(
  `\nFonts installed into ${outDir}.\nNow rebuild the runtime so the editor sees them: pnpm --filter @forge/player build:runtime`,
);
